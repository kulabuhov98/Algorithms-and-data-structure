	system ("chcp 1251");
	
	int basis, exponent; /* Целое число (основание), степень */
	
	cout << "Введите число: "; /* Сообщение пользователю */
	cin >> basis; /* Целое число (основание) */
		
	cout << "Введите степень: "; /* Сообщение пользователю */
	cin >> exponent; /* Целая степень целого числа */

	cout << "Число " << basis << " в степени " << exponent << " = " << pow(basis, exponent); /* Сообщение пользователю */
	
