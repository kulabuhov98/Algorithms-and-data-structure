using namespace std;

/* Функция вычисления целой степени целого числа */
int pow(int basis, int exponent) {
	if (exponent != 0) { /* Если целая степень числа не 0 */
		return (basis * pow(basis, exponent - 1)); /* Вычисление целой степени целого числа */
	} else {
		return 1; /* Если целая степень числа 0 */
	}
}

